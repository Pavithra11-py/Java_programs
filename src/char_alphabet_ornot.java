public class char_alphabet_ornot {
    public static void main(String[] args) {
        char ch='*';
        if (ch>='a' && ch<='z'|| ch >='A' && ch<='Z')
        {
            System.out.println("this is an alphabet");
        }
        else{
            System.out.println("this is not an alphabet");
        }
    }
}
